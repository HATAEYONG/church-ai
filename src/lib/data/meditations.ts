// 날짜 기준으로 '오늘의 말씀'을 정합니다 (매일 순환).
export function todaysPassage(date = new Date()): QtPassage {
  const dayOfYear = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000,
  );
  return qtPassages[dayOfYear % qtPassages.length];
}
